<template><div><h1 id="sort-by-column-header-row" tabindex="-1"><a class="header-anchor" href="#sort-by-column-header-row" aria-hidden="true">#</a> Sort by column header row</h1>
<p>The component allows <code v-pre>TDBVertGridEh</code> to sort line on the column headers fields. To specify the type of the sort use <code v-pre>TDBVertGridEh.RowsSortOrder</code> property. The <code v-pre>RowsSortOrder</code> property is of <code v-pre>TDBVertGridRowsSortOrderEh</code> enumeration type and can contain the following values:</p>
<dl>
<dd>
<p><code v-pre>vgsoByFieldRowIndexEh</code> 	Sort by order of the fields in the collection <code v-pre>TDBVertGridEh.Rows</code> (Sort by default).</p>
</dd>
<dd>
<p><code v-pre>vgsoByFieldRowCaptionAscEh</code>	Sort by Title fields Row[i].RowLabel.Caption ascending.</p>
</dd>
<dd>
<p><code v-pre>vgsoByFieldRowCaptionDescEh</code>	Sort by FIeldRow Title Row[i].RowLabel.Caption descending.</p>
</dd>
</dl>
</div></template>


