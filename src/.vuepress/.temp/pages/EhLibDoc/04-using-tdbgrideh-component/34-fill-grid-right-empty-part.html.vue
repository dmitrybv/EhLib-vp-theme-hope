<template><div><h1 id="fill-right-empty-part-of-tdbgrideh-with-cells-like-patterns" tabindex="-1"><a class="header-anchor" href="#fill-right-empty-part-of-tdbgrideh-with-cells-like-patterns" aria-hidden="true">#</a> Fill right empty part of TDBGridEh with cells like patterns</h1>
<p>New properties are added in the <code v-pre>DBGridEh</code> class that allows to fill the empty space at the right part of the grid with cells like pattern. See a screenshot.</p>
<p><img src="@source/images/clip0031.png" alt="" loading="lazy"></p>
<p>A new subproperty <code v-pre>HorzEmptySpaceStyle</code> of <code v-pre>TDBGridHorzEmptySpaceStyleEh</code> type is added to the <code v-pre>DBGridEh.GridLineParams</code> property.
A property can have the following values:</p>
<p><code v-pre>deshNonEh</code> – Do not fill the empty space to the right part of the grid.</p>
<p><code v-pre>deshExtendTitleWideSpaceEh</code> – Fill in the blank right header area of the grid.</p>
<p><code v-pre>deshExtendGridWideSpaceEh</code> – Fill in the blank right header area and data part of the grid.</p>
</div></template>


